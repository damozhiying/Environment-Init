#美化l

sudo dnf install gnome-tweak-tool

sudo dnf install nautilus-open-terminal

安装完成后，找到 “优化工具”，打开，看到左侧 “窗口” 没有，点击它，把右侧 “标题栏按钮”，最大化、最小化，把它们开启即可！

Fedora 25右键在当前目录位置打开终端

再点击左侧 “桌面”，把 “桌面图标” 给开启了，然后选择想要出现的图标。
点击左侧 “扩展”，可以把 window list 这个扩展给开启了，这样在屏幕底部能够看到当前打开的窗口，很方便地进行切换。

Fedora 25右键在当前目录位置打开终端

这样，我们就让 Fedora 25 更加人性化。